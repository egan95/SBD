    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {
            edit = new DialogEdit(this, true);
            int baris=tabelApotek.getSelectedRow();
      
            int id=(Integer) tabelApotek.getValueAt(baris, 0);
            apotek_a a = db.pilih_1_user(id);
            if(a!=null) {
                edit.setForm(a);
                edit.setVisible(true);
                refreshData();
            } else {
                JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di edit!","Error",JOptionPane.INFORMATION_MESSAGE);
        
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
       db = new database();
        try {
            
            int baris = tabelApotek.getSelectedRow();
            int id = (Integer) tabelApotek.getValueAt(baris, 0);
            String user = (String) tabelApotek.getValueAt(baris, 2);
            Object[] pilihan={"ya","Tidak"};
            int jawaban= JOptionPane.showOptionDialog(null,"Apakah anda yakin akan menhapus user "+user+"?", "Warning", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, pilihan, pilihan[0]);
            if(jawaban ==0) {
                try {
                    db.hapus_user(id);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
                }
                    refreshData();
            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di hapus!","Error",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

