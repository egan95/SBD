    private void btnKdeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKdeleteActionPerformed
        // TODO add your handling code here:
        db = new database();
        try {
            
            int baris = tabelKaryawan.getSelectedRow();
            String id = (String) tabelKaryawan.getValueAt(baris, 0);
            String nama = (String) tabelKaryawan.getValueAt(baris, 1);
            Object[] pilihan={"ya","Tidak"};
            int jawaban= JOptionPane.showOptionDialog(null,"Apakah anda yakin akan menhapus data karyawan dengan nama "+nama+"?", "Warning", JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE, null, pilihan, pilihan[0]);
            if(jawaban ==0) {
                try {
                    db.hapus_karyawan(id);
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(HalamanAdmin.class.getName()).log(Level.SEVERE, null, ex);
                }
                    refreshDataKaryawan();
                    refreshData();
            }
        } catch (Exception e) {

            JOptionPane.showMessageDialog(null, "Silahkan pilih data yang akan di hapus!","Error",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnKdeleteActionPerformed
    
