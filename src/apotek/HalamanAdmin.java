    private void btnOCariOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOCariOActionPerformed
        // TODO add your handling code here:
       try {
        caei_barang c = new caei_barang(this, true);
        c.setVisible(true);
        if ("id".equals(c.getCari())) {
            TBarang.setData(db.filter_Barang_id(c.getKunci()));
        } else if("nama".equals(c.getCari())){
             TBarang.setData(db.filter_Barang_nama(c.getKunci()));
        } else if ("suplier".equals(c.getCari())) {
            TBarang.setData(db.filter_Barang_suplier(c.getKunci()));
        }
        
        TBarang.fireTableDataChanged();
       } catch (Exception e) {
           System.out.print(e.getMessage());
       }
    }//GEN-LAST:event_btnOCariOActionPerformed

